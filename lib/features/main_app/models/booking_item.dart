class BookingItem {
  final String title;
  final String location;
  final int price;
  final double rating;
  final String dates;
  final String guests;
  final String image;

  const BookingItem({
    required this.title,
    required this.location,
    required this.price,
    required this.rating,
    required this.dates,
    required this.guests,
    required this.image,
  });
}
